export default async function TermsPage({
  searchParams,
}: {
  searchParams?: Promise<{ from?: string; step?: string }>;
}) {
  const resolvedSearchParams = await searchParams;
  const showBackButton = resolvedSearchParams?.from === "cart";
  const backHref =
    resolvedSearchParams?.step === "delivery" ? "/cart?step=delivery" : "/cart";

  return (
    <main className="min-h-screen bg-[radial-gradient(circle_at_top,_#fbf8fd_0%,_#f3edf7_45%,_#efe6f6_100%)]">
      <section className={pageSectionClassName}>
        <div className="mb-8">
          <SectionIntro
            title="Общи условия"
            titleAs="h1"
            size="page"
            description="Тук ще откриете основните правила за използване на сайта, подаването на поръчки, плащането, доставката и правата на потребителите."
            action={
              showBackButton ? (
                <Link href={backHref} className={sectionActionClassName}>
                  Обратно към количката
                </Link>
              ) : null
            }
          />
        </div>
      </section>

      <section className={contentSectionClassName}>
        <div className={contentSectionInnerClassName}>
          {termsSections.map((section, index) => (
            <article
              key={section.title}
              className={index === 0 ? "py-6 first:pt-0 last:pb-0" : "border-t border-[#ece4f1] py-6 first:pt-0 last:pb-0"}
            >
              <h2 className="text-xl font-semibold text-[#432855]">
                {section.title}
              </h2>
              <div className="mt-4 space-y-4 text-[15px] leading-7 text-[#5f4b73]">
                {section.paragraphs.map((paragraph) => (
                  <p key={paragraph}>{paragraph}</p>
                ))}
              </div>
            </article>
          ))}
        </div>
      </section>
    </main>
  );
}
